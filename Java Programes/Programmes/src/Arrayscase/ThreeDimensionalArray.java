package Arrayscase;

public class ThreeDimensionalArray {

	public static void main(String[] args) {

int[][][] f = {  { {1,2,3} ,{5,6,8} } ,{ {9,8,7},{3,2,1},{5,6,6,7,7,8,89,} }  };   


	
	for (int i=0;i<f.length;i++) 
	
	{
		for (int j=0;j<f[i].length;j++) 
		{
			for (int z=0;z<f[i][j].length;z++) 
			{
				System.out.print(  f[i] [j] [z] );
			}
			System.out.println();
		}
		
		
	}
		System.out.println();
	
	}


		
	}


