package FiftyPrograms;

public class AsciiOfChar {

	public static void main(String[] args) {
		
		char ch = 'n';
		
		int Ascii = ch;
		
		int CastAscii = (int) ch;
		
		
		System.out.println("The Ascii value of the character " + ch + " is " + Ascii );
		
		System.out.println("And the Ascii value of the Casted character " + ch + " is " + CastAscii );

	
	}

}
